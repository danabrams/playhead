    @Test("Exact score ties resolve through the oracle's (score, -moved, start, end) key — later end wins")
    func jointKeyOrderingResolvesExactTies() {
        let values = Self.syntheticTemplate()
        // Snap start to 95.0s; proposal [100, 170]. Derived partners at
        // 155 (2×30) and 185 (3×30) are EXACTLY symmetric around the
        // proposal end: identical bonus, identical movement (15s), same
        // start — a bitwise score tie. The oracle's strict lexicographic
        // key comparison then prefers the larger end time. This pins the
        // port's iteration-order/tie parity (a `>=` in the wrong place or
        // a reordered candidate list would flip it to 155).
        // Oracle X1 (0.9-cosine plant): [100, 170] → [95, 185] — same
        // tie, same resolution as this fixture's exact-copy plant.
