    // MARK: - Overlap guard (pair feasibility + revert belt-and-braces)

    @Test("A pair that abandons overlap is infeasible; joint picks the overlap-preserving snap instead")
    func pairFeasibilityRejectsOverlapAbandoningPair() throws {
        // v3 → v4 DELIBERATE contract change (oracle parity, X5): the v3
        // recipe snapped both edges to [20, 25], noticed the zero overlap
        // against the [80, 95] proposal, and reverted BOTH edges. The
        // joint recipe never builds that window: the (20, 25) pair is
        // infeasible at enumeration, and the best FEASIBLE pair keeps the
        // start snap while leaving the end on the proposal.
        // Oracle (refine_edges_joint, defaults): [80, 95] → [20, 95],
        // start_kind=peak, end_kind=none, pair_score 0.994.
        let preValues = Self.syntheticTemplate(seed: 11)
        let postValues = Self.syntheticTemplate(seed: 23)
        let pre = Self.template(preValues, edgeIndex: 300)
        let post = Self.template(postValues, edgeIndex: 50)
        let startEnvelope = Self.envelope(planting: preValues, at: 700, totalFrames: 9000, startSeconds: 0)
        let endEnvelope = Self.envelope(planting: postValues, at: 1200, totalFrames: 9000, startSeconds: 0)

        let result = StingerRefiner.refine(
            proposalStart: 80.0,
            proposalEnd: 95.0,
            entry: Self.entry(pre: pre, post: post),
            startEnvelope: startEnvelope,
            endEnvelope: endEnvelope,
            episodeDuration: 600.0
        )

        #expect(abs(result.startTime - 20.0) < 0.001, "the feasible pair keeps the start snap (got \(result.startTime))")
        #expect(result.endTime == 95.0, "the end candidate at 25.0 would abandon overlap — the end must stay on the proposal")
        #expect(!result.trace.revertedNoOverlap)
        #expect(result.trace.startSnapped)
        #expect(!result.trace.endSnapped)
        // The invariant the old revert test protected, stated directly:
        // the refined window ALWAYS overlaps the proposal.
        #expect(min(result.endTime, 95.0) - max(result.startTime, 80.0) > 0,
                "refinement must never abandon the presence evidence")
        let pairScore = try #require(result.trace.pairScore)
        #expect(abs(pairScore - 0.994) < 0.01, "oracle pair_score 0.994 (peak ~1.0 − 1e-4·60)")
    }

