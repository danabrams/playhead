    // MARK: - Grid (v4: derived candidates + pair scoring)

    @Test("A confident lone snap earns a grid-derived partner on the other edge")
    func gridApplicationWhenOneEdgeSnaps() throws {
        let values = Self.syntheticTemplate()
        // Snap start to 5 + (4200 + 300)/50 = 95.0s; proposal [100, 152].
        // v4: the confident (≥ 0.65) start anchor derives end candidates
        // at 95 + k·30; the on-grid (95, 155) pair earns the 0.5 bonus and
        // beats both the (95, 125) pair (more end movement) and the
        // no-partner (95, 152) pair (width 57 is within grid tolerance but
        // the untouched proposal edge is out of bonus scope).
        // Oracle X2 (0.9-cosine plant): [100, 152] → [95, 155],
        // end_kind=derived, grid_applied — same winning pair as this
        // fixture, whose exact-copy plant (NCC ≈ 1.0) scores
        // ~1.0 + 0.5 − 1e-4·8 ≈ 1.4992 (the oracle world's 0.9 peak
        // scores 1.3992; only the peak term differs).
        let pre = Self.template(values, edgeIndex: 300)
        let envelope = Self.envelope(planting: values, at: 4200, totalFrames: 9000, startSeconds: 5.0)
        let result = StingerRefiner.refine(
            proposalStart: 100.0,
            proposalEnd: 152.0,
            entry: Self.entry(pre: pre, grid: 30.0),
            startEnvelope: envelope,
            endEnvelope: nil,
            episodeDuration: 600.0
        )
        #expect(abs(result.startTime - 95.0) < 0.001, "got \(result.startTime)")
        #expect(abs(result.endTime - 155.0) < 0.001, "the derived on-grid partner must set end = start + 60 (got \(result.endTime))")
        #expect(result.trace.startSnapped)
        #expect(!result.trace.endSnapped)
        #expect(result.trace.gridApplied, "a derived candidate on the end edge reports gridApplied")
        let endDelta = try #require(result.trace.endDeltaSeconds)
        #expect(abs(endDelta - 3.0) < 0.001)
        #expect(result.trace.startCandidateCount == 1)
        // Derived ends at 125/155/185/215 honor the move cap (245 is 93s
        // from the proposal end and must be excluded).
        #expect(result.trace.endCandidateCount == 4)
        #expect(result.trace.gridTermApplied == "bonus")
        let pairScore = try #require(result.trace.pairScore)
        #expect(abs(pairScore - 1.4992) < 0.01, "peak ~1.0 + 0.5 bonus − 1e-4·8 movement")
    }

