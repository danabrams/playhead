    // MARK: - Joint oracle parity (playhead-xsdz.38)
    //
    // Port-fidelity scenarios: synthetic candidate worlds built with the
    // SAME exact-cosine planting the oracle's test harness uses
    // (scripts/tests/test_l2f_stinger_joint.py `PlantedEnvelopes`) — the
    // planted signal is `cos·û + sin·n̂ + 2.0` around the centered,
    // normalized template, so the NCC at the plant is EXACTLY the chosen
    // cosine and every score in a scenario is hand-computable. Expected
    // outcomes below were generated by RUNNING `refine_edges_joint`
    // (commit 5dc1b961) with default `JointConfig` on mirrored worlds;
    // each test quotes the oracle's output. The noise realizations differ
    // between python and Swift, but the planted cosines are exact in
    // both, so every DECISION margin is untouched by noise. Honest margin
    // note: against a 100-frame template the background NCC has σ ≈ 0.1,
    // so its maximum over a ±90s span sits around ~0.43 — the 0.50 gate
    // clears it by a modest ~0.07. The seeds are deterministic, so the
    // candidate-count assertions are stable, but a re-rolled noise world
    // (new seed / different Box-Muller libm) could need re-pinning of
    // counts; the chosen bounds and scores would not move.

    /// Deterministic standard normals: seeded LCG uniforms + Box-Muller.
    private struct SeededGaussians {
        private var state: UInt64
        private var cached: Double?

        init(seed: UInt64) {
            state = seed &+ 0x9E37_79B9_7F4A_7C15
        }

        private mutating func uniform() -> Double {
            state = state &* 6_364_136_223_846_793_005 &+ 1_442_695_040_888_963_407
            return Double(state >> 11) * 0x1.0p-53
        }

        mutating func next() -> Double {
            if let value = cached {
                cached = nil
                return value
            }
            var u1 = uniform()
            if u1 < .leastNormalMagnitude { u1 = .leastNormalMagnitude }
            let u2 = uniform()
            let radius = (-2.0 * Foundation.log(u1)).squareRoot()
            cached = radius * sin(2.0 * .pi * u2)
            return radius * cos(2.0 * .pi * u2)
        }
    }

    /// Mirror of the oracle harness's `PlantedEnvelopes`: a noisy quiet
    /// world (`N(0,1)·0.02 + 1.0`) with template patterns planted at
    /// exact NCC cosines.
    private struct PlantedWorld {
        let duration: Double
        private var frames: [Float]

        init(duration: Double, seed: UInt64 = 99) {
            self.duration = duration
            var rng = SeededGaussians(seed: seed)
            frames = (0..<Int(duration) * StingerEnvelope.envelopeHz).map { _ in
                Float(rng.next() * 0.02 + 1.0)
            }
        }

        /// Mirror of the oracle harness's `make_pattern`: 2 s of
        /// `N(0,1)·0.5 + 2.0`.
        static func pattern(seed: UInt64) -> [Double] {
            var rng = SeededGaussians(seed: seed)
            return (0..<(2 * StingerEnvelope.envelopeHz)).map { _ in
                rng.next() * 0.5 + 2.0
            }
        }

        /// Write a signal whose NCC against `pattern` at the plant
        /// position is EXACTLY `cosine` (Gram-Schmidt around the
        /// centered/normalized template, matching the oracle harness).
        mutating func plant(_ pattern: [Double], at seconds: Double, cosine: Double) {
            let mean = pattern.reduce(0, +) / Double(pattern.count)
            var u = pattern.map { $0 - mean }
            let uNorm = u.reduce(0) { $0 + $1 * $1 }.squareRoot()
            u = u.map { $0 / uNorm }
            var rng = SeededGaussians(seed: UInt64((seconds * 100).rounded()))
            var n = (0..<pattern.count).map { _ in rng.next() }
            let nMean = n.reduce(0, +) / Double(n.count)
            n = n.map { $0 - nMean }
            let projection = zip(n, u).reduce(0.0) { $0 + $1.0 * $1.1 }
            n = zip(n, u).map { $0.0 - projection * $0.1 }
            let nNorm = n.reduce(0) { $0 + $1 * $1 }.squareRoot()
            n = n.map { $0 / nNorm }
            let sine = (1.0 - cosine * cosine).squareRoot()
            let base = Int((seconds * Double(StingerEnvelope.envelopeHz)).rounded())
            for i in pattern.indices {
                frames[base + i] = Float(cosine * u[i] + sine * n[i] + 2.0)
            }
        }

        /// The ±90 s search envelope the service would hand the refiner
        /// for a proposal edge at `center` (same span math as
        /// `loadStingerSearchEnvelope` and the oracle's
        /// `joint_peak_candidates`).
        func envelope(center: Double) -> StingerSearchEnvelope {
            let spanStart = max(0.0, center - StingerRefiner.searchRadiusSeconds)
            let spanEnd = min(duration, center + StingerRefiner.searchRadiusSeconds)
            let hz = Double(StingerEnvelope.envelopeHz)
            let i = Int((spanStart * hz).rounded())
            let j = min(Int((spanEnd * hz).rounded()), frames.count)
            return StingerSearchEnvelope(values: Array(frames[i..<j]), startSeconds: spanStart)
        }
    }

